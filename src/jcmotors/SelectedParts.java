package jcmotors;

/**
 *
 * @author Ravindu
 */
public class SelectedParts {
    private String stock_id;
    private String part_name;
    private String type;
    private String model;
    private int quantity;
    private Double unit_price;
    private Double with_discount;
    private Double value;

    /**
     * @return the stock_id
     */
    public String getStock_id() {
        return stock_id;
    }

    /**
     * @param stock_id the stock_id to set
     */
    public void setStock_id(String stock_id) {
        this.stock_id = stock_id;
    }

    /**
     * @return the part_name
     */
    public String getPart_name() {
        return part_name;
    }

    /**
     * @param part_name the part_name to set
     */
    public void setPart_name(String part_name) {
        this.part_name = part_name;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the model
     */
    public String getModel() {
        return model;
    }

    /**
     * @param model the model to set
     */
    public void setModel(String model) {
        this.model = model;
    }

    /**
     * @return the quantity
     */
    public int getQuantity() {
        return quantity;
    }

    /**
     * @param quantity the quantity to set
     */
    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    /**
     * @return the unit_price
     */
    public Double getUnit_price() {
        return unit_price;
    }

    /**
     * @param unit_price the unit_price to set
     */
    public void setUnit_price(Double unit_price) {
        this.unit_price = unit_price;
    }

    /**
     * @return the with_discount
     */
    public Double getWith_discount() {
        return with_discount;
    }

    /**
     * @param with_discount the with_discount to set
     */
    public void setWith_discount(Double with_discount) {
        this.with_discount = with_discount;
    }

    /**
     * @return the value
     */
    public Double getValue() {
        return value;
    }

    /**
     * @param value the value to set
     */
    public void setValue(Double value) {
        this.value = value;
    }

 
    
    
    
}
